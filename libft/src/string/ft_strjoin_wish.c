char	*ft_join_wish(int ac, char **av)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < (ac - 2))
	{
		while (av[1][j])
			j++;
		av[1][j] = ' ';
		i++;
	}
	return (av[1]);
}
